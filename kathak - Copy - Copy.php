            <h1>Kathak</h1>
<br>
<p>
Kathak is one of the main genres of ancient Indian classical dance and is traditionally regarded to
 have originated from the travelling bards of North India referred as Kathakars or storytellers. 
 These Kathakars wandered around and communicated legendary stories via music, 
 dance and songs quite like the early Greek theatre. The genre developed during the Bhakti movement,
 the trend of theistic devotion which evolved in medieval Hinduism. The Kathakars communicate stories
 through rhythmic foot movements, hand gestures, facial expressions and eye work. This performing art that
 incorporates legends from ancient mythology and great Indian epics,
 especially from the life of Lord Krishna became quite popular in the courts of North Indian kingdoms.
 Three specific forms of this genre that is three gharanas (schools), which mostly differ
 in emphasis given to footwork versus acting, a
re more famous namely, the Jaipur gharana, the Benaras gharana and the Lucknow gharana
</p>
<br>

<h3>History & Evolution</h3>
<br>
The roots of this dance form trace back to Sanskrit Hindu text on performing arts called ‘Natya Shastra’ written by ancient Indian theatrologist and musicologist Bharata Muni. It is presumed that the first complete version of the text was completed between 200 BCE to 200 CE, but some sources mention the timeframe to be around 500 BCE and 500 CE. Thousands of verses structured in different chapters are found in the text that divides dance in two particular forms, namely ‘nrita’ that is pure dance which comprise of finesse of hand movements and gestures, and ‘nritya’ that is solo expressive dance that focuses on expressions.

Russian scholar Natalia Lidova states that ‘Natya Shastra’ describes various 
theories of Indian classical dances including Tandava dance of Lord Shiva, methods of acting, standing postures, 
gestures, basic steps, bhava and rasa. Mary Snodgrass states that the tradition of this dance form is traced back 
to the 400 BCE. Bharhut, a village in the Satna district of Madhya Pradesh, India stands as a representative of early Indian art. 
The 2nd century BC panels found there illustrates sculptures of dancers in different vertical poses with arm positions
 that resemble Kathak steps, many of which reflect the 'pataka hasta' Mudra. The word Kathak is deduced from the Vedic Sanskrit term
 ‘Katha’ which means ‘story’ while the term kathaka that finds place in several Hindu epics and texts means the person who tells a story. 
 Text-based analysis indicates Kathak as an ancient Indian classical dance form that presumably originated in 
Banaras or Varanasi and then spread its wings in Jaipur, Lucknow and many other regions of north and northwest India
<h3>Costumes</h3>
<br>
<p>
As Kathak is popular both in Hindu and Muslim communities 
the costumes of this dance form are made in line with traditions of the respective communities.
 There are two types of Hindu costumes for female dancers. 
 While the first one includes a sari worn in a unique fashion complimented 
 with a choli or blouse that covers the upper body and a scarf or urhni worn in some places, 
 the other costume includes a long embroidered skirt with a contrasting choli and a transparent urhni.
 Costume is well complimented with traditional jewellery, usually gold, that includes the ones adorning her hair,
 nose, ear, neck and hand. Musical anklets called ghunghru made of leather straps with small metallic bells attached to 
 it are wrapped in her ankles that produce rhythmic sound while she performs excellent and spectacular footwork. 
 Head jewellery adorns her in the second case. Vivid face make-up put on helps highlight her facial expressions. 
 Hindu male Kathak dancers usually wear a silk dhoti with a silk scarf tied on the upper part of the body which usually 
 remain bare or may be covered by a loose jacket. Jewellery of male dancers is quite simple compared to their female counterparts 
 and are usually made of stone.

The costume for Muslim female dancers includes a skirt along with a tight fitting trouser called 
churidar or pyjama and a long coat to cover the upper body and hands. A scarf covering the head compliments the whole attire which is completed with light jewellery.  
 <h3>Instruments & Music</h3>
<br>
<p>
A Kathak performance may include a dozen classical instruments depending more on
 the effect and depth required for a particular performance. However some instruments 
 are typically used in a Kathak performance like the tabla that harmonise well with the rhythmic 
 foot movements of the dancer and often imitates sound of such footwork movements or vice-versa to create a brilliant jugalbandi.
 A manjira that is hand cymbals and sarangi or harmonium are also used most often.
