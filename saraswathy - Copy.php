            <h1>Veena</h1>
<br>
<p>

Veena is a stringed instrument. 
It consists of a large body hollowed out of a block of wood. 
The stem of the instrument is also made of wood. 
The bridge is placed on the flat top of the body of the veena and the neck attached to 
 stem is usually carved into weird figure like the head of the dragon.

VEENAA gourd which is smaller than the rounded part of the body is fixed underneath the neck.
 Twenty one metallic frets are fixed on the stem by means of a resinous substance.

The veena has seven strings. Of them four are main strings that pass over the frets and are 
attached to the pegs of the neck. The other strings are used as side strings for rhythmic accompaniment.
 These strings pass over an arched bridge made of brass. They lie flat over the top of the body and are secured to the main bridge.

The veena is played by sitting cross-legged upon the floor and holding the veena in front.
 The small gourd on the left touch the left thigh, the left arm passing round the stem so that the fingers 
 rest easily upon the frets. The main body of the instrument is placed on the ground, partially supported by the right thigh.

There are different varieties of veena. Some of them are Mahanataka veena, Saraswati veena, Rudra veena
, Vichitra veena etc. Saraswati veena is known as the queen of veena's.
</p>
