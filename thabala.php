            <h1>Thabla</h1>
<br>
<p>

Tabla is one of the most famous instruments of India. 
It is said that drums appeared  early as 6-7th century AD. 
 Drums are seen in the Pushkaras depictions in the Ajanta sculptures. 

TABLAThe most interesting part of Indian drum is the loading of the leather surfaces,
 the right face carries a permanent loading. The tabla consists of two drums the 'bayan'
 played with the left hand and the 'dayan' played with the right hand. Bayan is made either
 of clay or copper while dayan is usually hollowed out of a block of wood. Both are covered with
 the skin fastened to leather hoops which are stretched over the body of the drum by means of leather braces.
 Cylindrical block of wood are wedged between the

down to lower or raise the pitch.
 The application of a mixture of flour and water to the left head of the dayan lowers
 the pitch and gives the dull bass sound. This plaster is always scraped off after use. 
 In bayan, the plaster is mixed with iron fillings and it is applied once for all. 

Tabla has a light and sweet sound. Therefore it is well suited for accompanying  kheyal,
 thumri and other soft instruments like sitar and sarod. Dayan can be tuned accurately, but bayan has an infinite pitch.
 It can be tuned accurately to an octave lower than tabla. The drums are kept erect on the ground and played with the fingers.
 Sound is produced by striking the center with full hand or the tip of fingers and press the bass of the palm towards simultaneously 
 sliding it over the drum head. Tabla has a highly developed technique of playing. 
 This instrument is capable of producing almost all the patterns of rhythms that a musician can conceive of.
 Eminent artists such as Ustad Zakir Hussain have made tabla well known around the world.
</p>
