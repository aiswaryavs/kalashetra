            <h1>Kuchipudi</h1>
<br>
<p>
Kuchipudi, a pre-eminent Indian classical dance form counted among ten leading classical dance forms of India, 
is a dance-drama performance art that originated in a village of Krishna district of Andhra Pradesh, India. 
Similar to all leading Indian classical dance forms, Kuchipudi too evolved as a religious art rooting back to the age-old Hindu Sanskrit text ‘Natya Shastra’ and connects traditionally with temples, spiritual faiths and travelling bards. This ancient dance form finds place in the 10th century copper inscriptions and in 15th century texts like ‘Machupalli Kaifat’. Traditionally it is regarded that the sanyassin of Advaita Vedanta sect, Tirtha Narayana Yati, and his disciple Siddhendra Yogi initiated, methodized and arranged the present day version of the dance form in 17th century. Usually performance repertoire of Kuchipudi that is broadly oriented on Lord Krishna and
 the tradition of Vaishnavism include an invocation, dharavu – short dance, nritta – pure dance and nritya – expressive dance respectively.
</p>
<br>

<h3>History</h3>
<br>
The theoretical foundation of Kuchipudi is rooted back to the ancient Sanskrit Hindu 
text on the performing arts called ‘Natya Shastra’ which is accredited to Indian theatrologist and
 musicologist Bharata Muni. It is assumed that the full version of the text was first completed between 200 BCE to 200 CE, 
 but such period also varies between 500 BCE and 500 CE. It incorporates verses in thousands that are structured
 in different chapters and divides dance in two distinct types that are ‘nrita’ that is pure technical dance and ‘nritya’
 that is solo expressive dance. ‘Natya Shastra’, states Russian scholar Natalia Lidova, explicates various 
 Indian classical dance theories including that of standing postures, bhava, rasa, basic steps, methods of acting, gestures and Tandava dance, which is associated with Lord Shiva. Bharata Muni not only mentions the Andhra region in this ancient text but also attributes an elegant movement called ‘Kaishiki vritti’ and a raga called ‘Andhri’ to this region. The raga that is associated with ‘Arsabhi’ 
and ‘Gandhari’ also finds place in several other Sanskrit texts dating back to the 1st millennium.
<h3>Costumes</h3>
<br>
<p>
While a male character wears dhoti, a female character wears a colourful sari 
that is stitched with a pleated cloth which opens like a hand fan when the dancer stretches or bends her 
legs while portraying spectacular footwork. Make-up is generally light complimented with traditional jewellery 
of the region which adorns her hair, nose, ear, arms and neck. A light metallic waist belt made of gold or brass 
adorns her waist while a leather anklet with small metallic bells called ghunghroo is wrapped on her ankles that produce 
rhythmic sounds while she performs brilliant footwork. Her hair is neatly braided and often beautified with flowers or done 
in tribhuvana style that depicts the three worlds. Her eye expressions are highlighted by outlining them with black collyrium. 
Sometimes special costumes and props are used for
 particular characters and plays, for example a peacock feathered crown adorns the dancer playing Lord Krishna.
 <h3>Instruments & Music</h3>
<br>
<p>
The ensemble of a Kuchipudi performance includes a Sutradhara or 
Nattuvanar who is the conductor of the entire performance. He recites the musical syllables and uses cymbals to produce rhythmic beat.
 The story or spiritual message is sung either by the conductor or another vocalist or sometimes by the actor-dancers.
 The musical instruments usually include cymbals, mridangam, tambura, veena and flute. 
