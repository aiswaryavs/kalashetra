            <h1>Mohiniyattam</h1>
<br>
<p>
Mohiniattam or Mohiniyattam is an Indian classical dance form that evolved in the state of Kerala,
 India, and is counted among the two popular dance arts of the state, the other being Kathakali. 
 Although its roots date back to the age-old Sanskrit Hindu text on performing arts called ‘Natya Shastra’, 
 similar to other Indian classical dance forms, Mohiniattam adheres to the Lasya type that showcases a more graceful,
 gentle and feminine form of dancing. Mohiniattam derives its name from the word ‘Mohini’, a female avatar of Lord Vishnu.
 Conventionally a solo dance performed by female artists, it emotes a play through dancing and
 singing where the song is customarily in Manipravala which is a mix of Sanskrit and Malayalam language and 
the recitation may be either performed by the dancer herself or by a vocalist with the music style being Carnatic.
</p>
<br>

<h3>History & Evolution</h3>
<br>
The theoretical foundation of this dance form like other major classical dance forms of 
India has its roots in sage Bharata Muni’s text called ‘Natya Shastra’, a Sanskrit Hindu text
 that deals with performing arts. The first complete version of ‘Natya Shastra’ is considered by some
 sources to have been completed between 200 BCE to 200 CE while some others mention the timeframe 
 between 500 BCE and 500 CE. It breaks dance into two specific types, the first one being ‘nritta’ or pure
 dance that centres around finesse of hand movements and gestures, and the other being ‘nritya’ that features
 the expressive aspect of dance. ‘Natya Shastra’ elucidates several theories of Indian classical dances including on
 standing postures, basic steps, bhava, rasa, methods of acting and gestures as also two forms of dance – the Tandava dance of Lord Shiva
 that exhibits more vigour and vitality and the Lasya dance that is more delicate and graceful.
 The Lasya dance theme and structure is followed in Mohiniattam.

Mohiniattam evolved from the state of Kerala which also has an association 
with the old tradition of Lasya style of dancing. The temple sculptures of the state are the earliest
 manifestations of Mohiniattam or other dance forms similar to it. Mohiniattam poses are also palpable from the various
 feminine sculptures that adorn the 11th century Vishnu temple at Trikodithanam, and the Kidangur Subramanya temple. 
 The Lasya theme was incorporated by Malayalam bards and playwrights, 
 a fact evident from the text-based records starting from the 12th century.
 The 16th century book titled ‘Vyavaharamala’ written by scholar, poet, author and astrologer Mazhamangalam Narayanan Namboodiri is the 
 first known book that mentions the term Mohiniyattam in connection with a payment due to a Mohiniyattam dancer. 
 While discussing about various performing art forms of Kerala,
 renowned poet Kunchan Nambiar in his 17th century book ‘Gosha Yatra’ mentioned about Mohiniyattam. 
 By that time this dance form had emerged as one of the classical art forms of the state. 
 The 18th century Sanskrit treatise ‘Balarama Bharatam’
 on natyam written by the king of Travancore Karthika Thirunal Bala Rama Varma
 (considered to be a significant secondary work on ‘Natya Shastra’) 
refers about ‘Mohino Natana’ among various other dance styles.
<h3>Costumes</h3>
<br>
<p>
The dancer wears a white or off-white plain sari embellished with bright golden 
or gold laced coloured brocade embroidered in its borders complimented with a matching choli or blouse.
 A pleated cloth having concentric golden or saffron coloured bands adorns the front part of the sari from the waist.
 This embellishment not only lets the artist perform her spectacular footwork flexibly but also highlights it,
 allowing the audience to watch it from a distance. She also wears a golden belt around her waist. 
 Jewellery adorns her head, hair, ears, neck, wrists and fingers.
 Musical anklets called ghunghru made of leather straps with small metallic bells attached to it are wrapped in her ankles. 
 These produce rhythmic sound while she showcases excellent footwork.
 Her feet and fingers are brightened with red coloured natural dyes so as to highlight gestures of her hands.
 Face make-up of the dancer is usually light with a Hindu tikka on her forehead while her lips are vividly coloured red
 and her eyes are lined prominently so as to make her eye movements more visible. Her hair tied typically on left side of
 her head is in tight round chignon hairstyle and beautified with flowers, usually jasmine, ringed around the bun.
 <h3>Instruments & Music</h3>
<br>
<p>
Vocal music of this performance art incorporates different rhythms and 
lyrics of many of the compositions performed in this dance form are in Manipravala that is a 
mix of Sanskrit and Malayalam language while the music style is Carnatic. Instruments played during 
a Mohiniattam performances usually comprise of Kuzhitalam or cymbals; Veena; Idakka, an hourglass-shaped drum;
 Mridangam, a barrel-shaped drum with two heads; and flute. 
